package com.jsp.decision;

public class Choice 
{
	public static void main(String args[])
	{
		int choice=1;
		switch (choice)
		{
		case 0 :
		{
		System.out.println("case 0 executing");
			}
		break;
		case 1:
		{
		System.out.println("case 1 executing");
		}
	    break;
		case 2:
		{
			System.out.println("case 2 executing");
		}
	    break ;
	    default :
	    {
	    	
	    
	        System.out.println("Wrong choice");    	
	    }
		}
			
	}

}
